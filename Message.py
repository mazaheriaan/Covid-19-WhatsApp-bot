def _WorldCloud(q):
    fileName='json/all/{}.json'.format(q)
    imgFile='json/img/{}.png'.format(q)
    if not os.path.exists(imgFile):
        if os.path.exists(fileName):
            with open(fileName,'r',encoding='utf-8') as f:
                data=json.load(f)
                myDict={}
                for t in data:
                    myDict[t['name']]=float(t['count'])
                wordcloud = WordCloud(color_func=grey_color_func, max_font_size=250, margin=0,background_color='gray', font_path='segoeui.ttf') .generate_from_frequencies(myDict)
                plt.imshow(wordcloud, interpolation='bilinear')
                plt.axis("off")
                plt.savefig(imgFile,bbox_inches='tight')
        else:
            return 'نام وارد شده اشتباه است.'
    return imgFile



