def PrettyPrint(x):
    x=sorted(x.items(),key=lambda item: item[1])
    x.reverse()
    x=collections.OrderedDict(x)
    return '\n'.join("{}-{} : {:,} نفر".format(index+1, k.replace('\n',''),int(v)) for index,(k,v) in enumerate(x.items()) )

